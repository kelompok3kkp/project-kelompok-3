    private void btnTampilkanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTampilkanActionPerformed
        String tanggalInput = tfTanggal.getText();
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID Transaksi");
    model.addColumn("Tanggal");
    model.addColumn("ID Pelanggan");
    model.addColumn("ID Karyawan");
    model.addColumn("ID Layanan");
    model.addColumn("Harga");
    model.addColumn("Diskon");
    model.addColumn("Metode");

    int total = 0;

    try {
        String sql = "SELECT * FROM transaksi_cuci WHERE tanggal_transaksi = ?";
        Connection conn = Koneksi.connect(); // âœ… Sesuai class kamu
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, tanggalInput);
        ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            int harga = rs.getInt("harga");
            int diskon = rs.getInt("diskon");
            int subtotal = harga - diskon;
            total += subtotal;

            model.addRow(new Object[]{
                rs.getString("id_transaksi"),
                rs.getString("tanggal_transaksi"),
                rs.getString("id_pelanggan"),
                rs.getString("id_karyawan"),
                rs.getString("id_layanan"),
                harga,
                diskon,
                rs.getString("metode_pembayaran")
            });
        }

        TableRiwayat.setModel(model);
        tp.setText("Total Hari Ini: Rp " + total);

    } catch (Exception e) {
        System.out.println("Error: " + e.getMessage());
    }

    }//GEN-LAST:event_btnTampilkanActionPerformed

