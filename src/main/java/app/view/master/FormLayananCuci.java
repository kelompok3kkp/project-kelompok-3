    private String generateIdLayanan(){
        String prefix = "L";
        String sql = "SELECT id_layanan FROM layanan_cuci ORDER BY id_layanan DESC LIMIT 1";
        try {
            Statement stat = koneksi.createStatement();
            ResultSet rs = stat.executeQuery(sql);
            if (rs.next()) {
                String lastId = rs.getString("id_layanan");
                int number = Integer.parseInt(lastId.substring(1));
                number++;
                return String.format(prefix + "%03d", number);
            } else {
                return prefix + "001";
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal generate ID: " + e);
            return null;
        }
    }
    
    protected void datatable(){
        Object[] Baris = {"ID Layanan", "Jenis Kendaraan", "Model Kendaraan", "Jenis Layanan", "Keterangan", "Harga"};
            model = new DefaultTableModel(null, Baris);
            String cariitem = txtcari.getText();
            
            try {
                String sql = "SELECT *FROM layanan_cuci WHERE id_layanan LIKE '%" + cariitem + "%' " 
                        + "OR jenis_kendaraan LIKE '%" + cariitem + "%' " 
                        + "OR model_kendaraan LIKE '%" + cariitem + "%' " 
                        + "OR jenis_layanan LIKE '%" + cariitem + "%' " 
                        + "OR keterangan LIKE '%" + cariitem + "%' "
                        + "OR harga LIKE '%" + cariitem + "%' "
                        + "ORDER BY id_layanan ASC";
                Statement stat = koneksi.createStatement();
                ResultSet hasil = stat.executeQuery(sql);
                
                while (hasil.next()){
                    model.addRow(new Object[]{
                        hasil.getString(1),
                        hasil.getString(2),
                        hasil.getString(3),
                        hasil.getString(4),
                        hasil.getString(5),
                        hasil.getString(6),
                    });
                }
                tablelayanan.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data gagal dipanggil"+e);
        }
    }
