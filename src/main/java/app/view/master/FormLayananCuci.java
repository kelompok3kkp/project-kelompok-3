    private void btnsimpan1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpan1ActionPerformed
        // TODO add your handling code here:
        String jenis = null;
        String id = txtid.getText().trim();
        String harga = txtharga.getText().trim();

        if(jmotor.isSelected()){
            jenis = "motor";
        } else if (jmobil.isSelected()){
            jenis = "mobil";
        }

        if (id.isEmpty() || jenis == null || cbmodel.getSelectedItem() == null || cblayanan.getSelectedItem() == null || harga.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Semua data layanan harus terisi.");
            txtid.requestFocus();
            return;
        }

        String cekSql = "SELECT id_layanan FROM layanan_cuci WHERE id_layanan = ?";
        try {
            PreparedStatement cekStat = koneksi.prepareStatement(cekSql);
            cekStat.setString(1, id);
            ResultSet rs = cekStat.executeQuery();
            if (rs.next()) {
                JOptionPane.showMessageDialog(null, "ID layanan sudah digunakan.");
                txtid.requestFocus();
                return;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat mengecek ID: " + e);
            return;
        }

        String sql = "INSERT INTO layanan_cuci (id_layanan, jenis_kendaraan, model_kendaraan, jenis_layanan, keterangan, harga) VALUES (?,?,?,?,?,?)";
        try {
            PreparedStatement stat = koneksi.prepareStatement(sql);
            stat.setString(1, txtid.getText());
            stat.setString(2, jenis);
            stat.setString(3, cbmodel.getSelectedItem().toString());
            stat.setString(4, cblayanan.getSelectedItem().toString());
            stat.setString(5, txtketerangan.getText());
            stat.setDouble(6, Double.parseDouble(harga));

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data layanan berhasil disimpan.");
            kosong();
            txtid.requestFocus();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data layanan gagal disimpan. " + e);
        }
        datatable();
    }//GEN-LAST:event_btnsimpan1ActionPerformed

    private void btnedit1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnedit1ActionPerformed
        // TODO add your handling code here:
        String jenis = null;
        String id = txtid.getText().trim();
        String harga = txtharga.getText().trim();

        if (jmotor.isSelected()) {
            jenis = "motor";
        } else if (jmobil.isSelected()) {
            jenis = "mobil";
        }

        if (id.isEmpty() || jenis == null || cbmodel.getSelectedItem() == null || cblayanan.getSelectedItem() == null || harga.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Semua data layanan harus terisi.");
            txtid.requestFocus();
            return;
        }

        try {
            String sql = "UPDATE layanan_cuci SET jenis_kendaraan=?, model_kendaraan=?, jenis_layanan=?, keterangan=?, harga=? WHERE id_layanan=?";
            PreparedStatement stat = koneksi.prepareStatement(sql);
            stat.setString(1, jenis);
            stat.setString(2, cbmodel.getSelectedItem().toString());
            stat.setString(3, cblayanan.getSelectedItem().toString());
            stat.setString(4, txtketerangan.getText());
            stat.setDouble(5, Double.parseDouble(harga));
            stat.setString(6, id);

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data layanan berhasil diubah.");
            kosong();
            txtid.requestFocus();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data layanan gagal diubah: " + e);
        }
        datatable();
    }//GEN-LAST:event_btnedit1ActionPerformed

    private void btnhapus1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapus1ActionPerformed
        // TODO add your handling code here:
        int ok = JOptionPane.showConfirmDialog(null, "Hapus data layanan ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (ok == 0) {
            String id = txtid.getText().trim();
            if (id.isEmpty()) {
                JOptionPane.showMessageDialog(null, "ID Layanan tidak boleh kosong.");
                txtid.requestFocus();
                return;
            }

            String sql = "DELETE FROM layanan_cuci WHERE id_layanan = ?";
            try {
                PreparedStatement stat = koneksi.prepareStatement(sql);
                stat.setString(1, id);

                int rowsAffected = stat.executeUpdate();
                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(null, "Data layanan berhasil dihapus.");
                    kosong(); // method clear input
                    txtid.requestFocus();
                } else {
                    JOptionPane.showMessageDialog(null, "Data tidak ditemukan atau sudah dihapus.");
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data layanan gagal dihapus: " + e.getMessage());
            }
            datatable();
        }
    }//GEN-LAST:event_btnhapus1ActionPerformed

